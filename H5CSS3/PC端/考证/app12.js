const id = "100"

// == 只判断值是否相等
// 判断是否相等
if (id == 100) {
    console.log("正确")
} else {
    console.log("不正确")
}

// 判断是否不相等
if (id !== 101 ) {
    console.log("正确")
} else {
    console.log("不正确")
}

// === 判断值和类型是否相等
if (id === 100) {
    console.log("正确")
} else {
    console.log("不正确")
}